                <p>It was my mother who took me to the police station.

The officers had wanted to drive me in the back of their squad car, but she told them no. It’s the only time I can remember her losing her temper. I was fifteen years old, standing in the kitchen, flanked by two huge policemen. My mother was in the doorway. I remember her expression changing as they told her why they were there and what they wanted to talk to me about. At first she seemed confused by what she was hearing, but then her face shifted to fear as she looked at me and saw how lost and scared I was right at that moment.

And, while my mother was a small woman, something in the quiet ferocity of her voice and the strength of her posture caused both of those huge policemen to take a step back from me. On the way to the police station, I sat in the passenger seat beside my mother, feeling numb as we followed the car that was escorting us through the town.

It slowed as we reached the old playground.

“Don’t look,” my mother told me.

But I did. I saw the cordons that had been put in place. The officers lining the street, their faces grim. All the vehicles that were parked along the roadside, their lights rotating silently in the late afternoon sun. And I saw the old jungle gym. The ground beside it had always been dull and gray before, but right now I could see it was patterned in red. It all seemed so quiet and solemn, the atmosphere almost reverential.

And then the car ahead of us came to a stop.

The officers were making sure I got a good look at a scene they were certain I was responsible for.

You have to do something about Charlie.

It was a thought I’d had a great deal in the months leading up to that day, and I still remember the frustration it always brought. I was fifteen years old, and it wasn’t fair. Back then, it felt like my entire life was constrained and controlled by the adults around me, and yet none of them appeared to have noticed the black flower rotting in the middle of the yard. Or else they had decided it was easier to leave it alone—that the grass it was poisoning didn’t matter.

It should not have been left to me to deal with Charlie.

I understand that now.

And yet, as I sat in the car right then, the guilt they wanted me to feel overwhelmed me. Earlier that day, I had been walking through the dusty streets, squinting against the sun and sweating in the simmering heat, and I had spotted James right there in the playground. My oldest friend. A small, lonely figure in the distance, perched awkwardly on the jungle gym. And while it had been weeks by then since he and I had spoken, I had known full well what he was doing. That he was waiting there for Charlie and Billy.

A number of the officers at the scene turned to look at us, and for a moment I felt trapped in a pocket of absolute silence. Stared at and judged.

Then I flinched as a sudden noise filled the air.

It took me a second to realize that my mother was leaning on the car horn. The blaring volume of the sound seemed jarring and profane in the setting—a scream at a funeral—but when I looked at her I saw my mother’s jaw was clenched and her gaze directed furiously at the police car ahead. She kept her hand pressed down, and the sound continued, echoing around the town.

Five seconds.

“Mom.”

Ten seconds.

“Mom.”
Then the police car in front of us began moving slowly away again. My mother lifted her hand from the horn and the world fell quiet. When she turned to me, her expression was somehow both helpless and resolute at the same time, as though my hurt were her own and she was determined to bear the weight of it for me as much as she could.

Because I was her son, and she was going to look after me.

“It’s going to be okay,” she said.

I did not reply. I just stared back, recognizing the seriousness in her voice and the conviction on her face, and feeling grateful that there was someone there to look after me, even if I would never have admitted it. Grateful there was someone with me who cared about me. Someone who had such faith in my innocence that the words themselves didn’t need to be spoken out loud.

Someone who would do anything to protect me.

After what felt like an age, she nodded to herself, and began driving. We followed the car out of the town and left the parked police vans, the staring officers, and the bloodstained playground behind us. And my mother’s words were still echoing in my head as we reached the main road.

It’s going to be okay.

Twenty-five years have passed, but I still think about that a lot. It’s what all good parents tell their children. And yet what does it really amount to? It’s a hope, a wish. A hostage to fortune. It’s a promise you have to make, and one you must do your best to believe in, because what else is there?

It’s going to be okay.

Yes, I think about that a lot.

