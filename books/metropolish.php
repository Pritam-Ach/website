                <p>Now the rumbling of the great organ swelled to a roar, press-

                    ing, like a rising giant, against the vaulted ceiling, to burst

                    through it.

                    Freder bent his head backwards, his wide-open, burning eyes

                    stared unseeingly upward. His hands formed music from the

                    chaos of the notes; struggling with the vibration of the sound

                    and stirring him to his innermost depths.

                    He was never so near tears in his life and, blissfully helpless,

                    he yielded himself up to the glowing moisture which dazzled

                    him.

                    Above him, the vault of heaven in lapis lazuli; hovering

                    therein, the twelve-fold mystery, the Signs of the Zodiac in

                    gold. Set higher above them, the seven crowned ones: the plan-

                    ets. High above all a silver-shining bevy of stars: the universe.

                    Before the bedewed eyes of the organ-player, to his music,

                    the stars of heavens began the solemn mighty dance.

                    The breakers of the notes dissolved the room into nothing.

                    The organ, which Freder played, stood in the middle of the sea.

                    It was a reef upon which the waves foamed. Carrying crests

                    of froth, they dashed violently onward, and the seventh was al-

                    ways the mightiest.

                    But high above the sea, which bellowed in the uproar of the

                    waves, the stars of heaven danced the solemn, mighty dance.

                    Shaken to her core, the old earth started from her sleep. Her

                    torrents dried up; her mountains fell to ruin. From the ripped

                    open depths the fire welled up; The earth burnt with all she

                    bore. The waves of the sea became waves of fire. The organ

                    flared up, a roaring torch of music. The earth, the sea and the hymn-blazing organ crashed in and became ashes.

                    But high above the deserts and the spaces, to which creation

                    was burnt, the stars of heaven danced the solemn mighty

                    dance.

