                    Then, from the grey, scattered ashes, on trembling wings un-

                    speakably beautiful and solitary, rose a bird with jewelled

                    feathers. It uttered a mournful cry. No bird which ever lived

                    could have mourned so agonisingly.

                    It hovered above the ashes of the completely ruined earth. It

                    hovered hither and thither, not knowing where to settle. It

                    hovered above the grave of the sea and above the corpse of the

                    earth. Never, since the sinning angel fell from heaven to hell,

                    had the air heard such a cry of despair.

                    Then, from the solemn mighty dance of the stars, one freed

                    itself and neared the dead earth. Its light was gentler than

                    moonlight and more imperious than the fight of the sun.

                    Among the music of the spheres it was the most heavenly note.

                    It enveloped the mourning bird in its dear light; it was as

                    strong as a deity, crying: "To me… to me!"

                    Then the jewelled bird left the grave of the sea and earth and

                    gave its sinking wings up to the powerful voice which bore it.

                    Moving in a cradle of light, it swept upwards and sang, becom-

                    ing a note of the spheres, vanishing into Eternity…

                    Freder let his fingers slip from the keys. He bent forward and

                    buried his face in his hands. He pressed his eyes until he saw

                    the fiery dance of the stars behind his eyelids. Nothing could

                    help him—nothing. Everywhere, everywhere, in an agonising,

                    blissful omnipresence, stood, in his vision, the one one countenance.

